        <p>В кабинете открыта Школа по профилактике и развитию близорукости у детей школьного возраста. Амбулаторно
            проводится консервативное лечение близорукости у детей: индивидуальные схемы медикаментозного лечения,
            тренировка аккомодации по методу Аветисова-Мац и физиотерапевтическое лечение на аппарате ИНФИТА-М, а также
            выявляются показания к стационарному лечению.</p>

        <p>Также в кабинете проводится диагностика и диспансерное наблюдение детей и подростков с патологией рефракции
            (близорукость, дальнозоркость, астигматизм), амблиопией, косоглазием, патологией сетчатки и оптических
            сред.</p>
        <p>Близорукость (миопия), особенно  у детей дошкольного и школьного возраста –  одна из актуальнейших проблем  офтальмологии . Так как является основной причиной зрительных расстройств у молодых людей.  Поэтому проблема реабилитации детей и подростков с миопией безусловно является социально значимой.</p>
        <p>Миопия - заболевание, при котором дети плохо различают предметы, расположенные на дальнем расстоянии. Миопия – одно из самых распространенных заболеваний у детей дошкольного и школьного возраста. К возрасту 14-15 лет практически каждый третий ребенок страдает близорукостью.</p>

        <h3><b>Почему разумнее начать лечение близорукости как можно раньше?</b></h3>
        <p>Чем раньше будет начато лечение прогрессирующей миопии и спазма аккомодации,тем больше возможностей для предупреждения развития осложнений на глазном дне и безвозвратной утери зрения.</p>
        <p>В нашей поликлинике функционирует «Школа по профилактике Миопии», обеспечивающая проведение комплексного аппаратного и  медикаментозного лечения. Проводится совместная работа с ортопедами, неврологами, врачами кабинета медицинской реабилитации (обеспечивающими проведение массажа шейно -воротниковой зоны и волосистой части головы)</p>
        <p>Схема лечения и восстановления зрения при близорукости у детей определяется степенью нарушения, ее развитием и наличием осложнений.</p>

        <h3><b>Методы лечения:</b></h3>
        <h4>Физиотерапевтические методы лечения миопии и спазма аккомодации:</h4>
        <ul>
            <li><b>Вакуумные очки сидоренко амво-01</b></li>
            <p></p>
            <img class="det_otd_department_logo" src="img/det/och.jpg" alt="Вакуумные очки сидоренко амво-01">
            <p></p>
            <p>Лечебный эффект АМВО-01 Очки Сидоренко основан на применении мягкого вакуумного массажа глазного яблока, глазодвигательных мышц и окружающих тканей.
                Имеет встроенные излучатели (Очки Панкова "Радуга прозрения"), обеспечивающие дополнительные терапевтические свойства.
            </p>
            <p>Цвет является специфическим раздражителем глаза. Терапия с использованием специальных искусственных цветовых излучателей  называется методом визуальной цветостмуляции. При этом осуществляется комплексное воздействие 4 факторов: цвета, ритма, модуляции цветовых сигналов и уровня освещенности светового поля.
                Очки Сидоренко дают великолепный эффект при лечении глазной патологии у детей  и не вызывают побочных эффектов.
            </p>
            <li><b>Магнитотерапия</b> в офтальмологии стала активно применяться в 70-е годы. Доказано выраженное противоотечное, спазмолитическое, рассасывающее действие магнитного поля, стимулирующее действие его на фоторецепторы сетчатки, улучшение микроциркуляции и  метаболизма.</li>
            <li><b>Тренировка аккомодации по методике Аветисова -Мац.</b></li>
            <p>Методика достаточно проста. Пациента просят в течении 3 минут читать текст номер 4  в очках с линзами , корректирующими близорукость , плюс линзы , равные запасу относительной аккомодации  -0,5 D. если в течении этого времени у пациента не возникло  никаких неприятных ощущений  и не потерялась четкость зрения , то ставят более сложные линзы.</p>
            <ul>
                <li><b>Оптическая коррекция.</b> В первую очередь ребенку подбирают очки или контактные линзы (в старшем возрасте). При близорукости слабой или средней степени оптическая коррекция нужна только для дали. В случае миопии свыше 6 D или при ее прогрессировании очки нужно носить постоянно.</li>
                <li><b>Хирургическое вмешательство.</b> Если у ребенка диагностирована близорукость высокой степени или ее прогрессирующая форма, может быть назначено оперативное лечение – склеропластика, которое направлено на укрепление склеры. Лазерная коррекция миопии у детей проводится по достижению пациентом 21 года.</li>
            </ul>
