            получает до 90% информации. В детском возрасте развитие органов зрения, как и других органов и систем,
            происходит очень быстро. И крайне важно своевременно обнаружить наличие той или иной патологии и назначить
            адекватное лечение.</p>

        <p>Рады сообщить, что в нашей клинике всем детям можно провести  биомикрофотографическое исследование глазного дна на самой современной диагностической аппаратуре «Цифровой ретинальной камере(фундус-камера)» и по результатам получить консультацию высококлассного специалиста- врача-офтальмолога!</p>
        <p>Биомикрофотография глазного дна посредством фундус-камеры является наиболее эффективным способом визуализации состояния внутренних поверхностей глазного дна, включая поверхности сетчатки, а также диска зрительного нерва.</p>
        <p>Патологии сетчатки и зрительного нерва - основные причины необратимой потери зрения. Поэтому так необходима ранняя диагностика, позволяющая своевременно начать лечение, которое  предотвращает или снижает степень потери зрения.Фотография глазного дна при помощи фундус-камеры имеет ряд диагностических преимуществ. Так фундус-камера позволяет:</p>
        <ul>
            <li>Визуализировать состояние глазного дна без предварительного расширения зрачка и введения контраста, что ускоряет и упрощает обследование.</li>
            <li>Накапливать архив изображений, оценивая динамику состояния сетчатки и зрительного нерва, что позволяет оценить эффективность лечения и назначить его корректировку.</li>
            <li>Простота и легкость получения снимков у детей при диагностике</li>
            <li>Пациент наглядно видит картину его глазного дна для обсуждения целей и задач лечения</li>
        </ul>
