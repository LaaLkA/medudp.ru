        <h3><b>Об отделениии:</b></h3>
        <p>Отделение организовано в 2018г. и оснащено современным медицинским оборудованием, позволяющим проводить все
            виды функциональной и ультразвуковой диагностики (ЭКГ, суточное мониторирование ЭКГ и АД, ВЭМ, ЭЭГ с
            функциональными пробами, ЭЭГ-мониторинг дневного сна, РЭГ, ЭХО-ЭГ, спирография с фармакологическими пробами,
            электронейромиография, УЗИ) и предоставлять широкий спектр реабилитационных мероприятий детям от 1 месяца
            жизни до 18 лет: физиотерапия - магнитотерапия, светолечение (лазеротерапия, УФО), ультразвуковая терапия,
            УВЧ, электролечение (электростимуляция, электрофорез, ультратон, дарсанваль), массаж и ЛФК. </p>
        <p>В отделении работают опытные высококвалифицированные врачи функциональной и ультразвуковой диагностики,
