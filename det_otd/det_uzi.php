        <h3><b>Кабинет ультразвуковой диагностики</b></h3>
        <p>Ультразвуковая диагностика (УЗИ) – самый доступный и безопасный метод, позволяющий оценить строение и работу внутренних органов в режиме реального времени, как в норме, так и при различных заболеваниях. Полное отсутствие ионизирующей радиации, абсолютная безболезненность метода дает возможность поводить исследование детям в любом возрасте.</p>
        <p>Кабинет ультразвуковой диагностики оснащен современным оборудованием премиум класса, позволяющим проводить все методы ультразвукового исследования детям от рождения до 18 лет. В кабинете работают высококвалифицированные врачи, имеющие степень кандидата медицинских наук и высшую квалификационную категорию. </p>
        <h3><b>Виды ультразвуковых исследований:</b></h3>
        <ul>
            <li><b>УЗИ головного мозга новорожденного - нейросонография (НСГ).</b> Исследование является обязательным на первом году жизни ребенка для выявления нарушения развития головного мозга. </li>
            <br>
            <li><b>УЗИ тазобедренных суставов новорожденных</b>. Исследование необходимо провести каждому новорожденному ребенку до 3 месяцев жизни для исключения врожденного вывиха бедра и дисплазии. Своевременная диагностика поможет врачу назначить правильное лечение и избежать инвалидизации.</li>
            <br>
            <li><b>Эхокардиография (ЭХО-КГ)</b> – метод диагностики морфологических и функциональных изменений сердца, его клапанного аппарата и магистральных сосудов. Позволяет выявить врожденные пороки и аномалии сердца, а также нарушение сократительной функции миокарда у часто болеющих простудными заболеваниями детей. Подростки, занимающиеся спортом должны ежегодно проходить ультразвуковое исследование сердечно-сосудистой системы.</li>
            <br>
            <li><b>УЗД сосудов головного мозга и шеи - (ТКДГ, транскраниальная допплерография, дуплексное сканирование)</b> – это метод в детской практике направлен на выявление аномалий развития брахицефальных сосудов на различном уровне (сонных и позвоночных артерий, мозговых артерий и вен) и оценку выраженности внутричерепной гипертензии при головных болях, синкопальных состояниях и травмах головы и шеи.</li>
            <br>
            <li><b>УЗИ органов брюшной полости (печень, желчный пузырь, поджелудочная железа, селезенка) и полых органов (желудок и 12-перстная кишка с проведением функциональной-водно-сифонной пробы).</b> Диагностика органов брюшной полости помогает выявить аномалии развития, острую хирургическую патологию, функциональные нарушения желудочно-кишечного тракта у детей различного возраста, определить степень выраженности интоксикации при острой вирусной и бактериальной инфекции.
                <br>
                <br>
                <ins>Важно, требуется подготовка! Исследование предпочтительно проводить натощак (у детей до года перерыв в кормлении не менее 2-3 часов), у более старших детей период голода должен быть от 4 до 10 часов. Накануне исследования исключить из пищи овощи, фрукты, молочные продукты, хлебобулочные изделия.</ins>
            </li>
            <br>
            <li><b>УЗИ почек и надпочечников, мочевого пузыря с определение остаточной мочи/</b> Исследование мочевыделительной системы информирует лечащего врача о наличии врожденных аномалий развития (дистопия, единственная почка и т.д.), не влияющих на функцию организма в целом, степень выраженности воспалительных процессов (пиелонефрит, цистит) и частых неврологических синдромов (нейрогенный мочевой пузырь). Для оценки выделительной функции почек и мочевого пузыря требуется специальная подготовка – наполнение мочевого пузыря (прием жидкости за час до исследования).
            </li>
            <br>
            <li><b>УЗИ щитовидной железы </b>– исследование позволяет выявить нарушение эндокринной системы у ребенка в различные возрастные периоды. </li>
            <br>
            <li><b>УЗИ молочных желез, матки и яичников (узи малого таза) у девочек и органов мошонки у мальчиков</b> — важнейший метод диагностики многочисленных заболеваний, связанных с половой сферой. Это безболезненное и безопасное исследование производится через поверхность кожи и позволяет получить четкое изображение органов репродуктивной системы. Проводится детям разного возраста для исключения врожденной аномалии и выявления признаков гормональной дисфункции как в раннем грудном возрасте, так и в периоды полового созревания. </li>
            <br>
            <ins>Важно! Исследование малого таза у девочек проводится с наполненным мочевым пузырем!</ins>
            <br>
            <br>
            <li><b>УЗИ слюнных желез и лимфатических узлов, вилочковой железы, мягких тканей (подкожно-жировая клетчатка и мышцы любой локализации, передняя брюшная стенка), костно-мышечной системы (лучезапястные, локтевые, плечевые, тазобедренные, коленные, голеностопные суставы и мышцы) и плевральных полостей (плевральный выпот при различных состояниях)</b> проводятся по назначению специалиста. Для оценки изменений в динамике желательно иметь с собой результаты предыдущего исследования.</li>
        </ul>

