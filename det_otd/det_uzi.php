        <h3><b>Виды исследований: </b></h3>
        <ul>
            <li><b>Электрокардиография (ЭКГ)</b> – запись электрической активности сердца. Позволяет выявить различные нарушения сердечного ритма, в том числе оценить состояние сердечно-сосудистой системы после перенесенных острых респираторных заболеваний.</li>
            <br>
            <li><b>Холтеровское мониторирование ЭКГ</b> – запись электрокардиограммы в течение суток, позволяет непрерывно наблюдать за работой сердца в дневное и ночное время, на фоне физических и эмоциональных нагрузок, что максимально информативно для диагностики заболеваний, протекающих бессимптомно или приступообразно.</li>
            <br>
            <li><b>Суточное мониторирование артериального давления (СМАД)</b> – позволяет выявить повышенное артериальное давление у детей, помогает врачу отличить ситуационное повышение давления, например при психоэмоциональной перегрузке, от истинной гипертонической болезни.</li>
            <br>
            <li><b>Велоэргометрия (ВЭМ)</b> – предусматривает регистрацию частоты сердечных сокращений (ЭКГ) и артериального давления (АД) в условиях возрастающих дозированных физических нагрузок, позволяет определить индивидуальную толерантность к физической нагрузке, выявить нарушения сердечного ритма на высоте нагрузки. ВЭМ целесообразно проводить детям, занимающимся спортом.</li>
            <br>
            <li><b>Электроэнцефалография (ЭЭГ)</b> – запись электрической активности коры мозга. Проводится во время бодрствования ребенка, прежде всего для выявления эпилептической активности. В исследование входят различные пробы, помогающие оценивать работу ЦНС во время воздействия фото-раздражителей и гипервентиляции.</li>
            <br>
            <li><b>Дневной видео-ЭЭГ мониторинг со сном</b> – запись электрической активности мозга во сне, проводится детям без эпилептической активности, у которых наблюдаются речевые расстройства, нарушения поведения, различные навязчивые движения, нарушения сна, энурез, нередко приводящие к социальной дезадаптации. Часто причина таких нарушений обнаруживается при первой же регистрации ЭЭГ во сне, несмотря на отрицательные результаты всех предыдущих исследований, проведенных в бодрствовании. </li>
            <br>
            <li><b>Реоэнцефалография (РЭГ)</b> – это метод оценки кровообращения в мозге, единственная методика, позволяющая оценить сосудистый тонус в мелких сосудах головного мозга.</li>
            <br>
            <li><b>Эхоэнцефалография (Эхо-ЭГ)</b> – позволяет оценить уровень внутричерепного давления, является очень распространенной методикой в детской практике.</li>
            <br>
            <li><b>Электронейромиграфия (ЭНМГ)</b> – исследование позволяет оценить проведение нервного импульса по периферическим нервам рук и ног, определить уровень и степень поражения нервов и мышц. Необходимо проводить пациентам с жалобами на снижение мышечной силы в руках и ногах, на онемение в кистях и стопах.</li>
            <br>
            <li><b>Исследование функции внешнего дыхания (ФВД или спирометрия)</b> – позволяет определить показатели легочной деятельности, такие как дыхательные объемы и потоки. Помимо форсированной спирометрии, у детей с бронхиальной астмой и обструктивными заболеваниями легких востребована спирография с фармакологическими пробами (с применением бронхолитиков).</li>
        </ul>

