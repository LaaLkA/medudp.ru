        <h3><b>Кабинет функциональной диагностики</b></h3>

        <p><b>Функциональная диагностика</b> – это информативный, безопасный, и самое важное, неинвазивный метод исследования, позволяющий эффективно и своевременно оценивать функциональное состояние сердечно-сосудистой, нервной и дыхательной систем органов ребенка. Ранняя диагностика отклонений работы главных систем органов позволяет выбрать наиболее эффективную тактику лечения и предупредить прогрессирование заболевания. Методы функциональной диагностики также широко используются при диспансерных осмотрах у детей и подростков.</p>
        <p>Кабинет функциональной диагностики оснащен современным оборудованием, с помощью которого можно проводить все методы исследования детям различного возраста. В кабинете работают высококвалифицированные врачи, имеющие высшую квалификационную категорию.</p>
