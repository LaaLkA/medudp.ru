                    <li>при «беспричинном» повышении температуры тела</li>
                    <li>для исключения аномалий развития мочеполовых органов (гидронефроз, пузырно-мочеточниковый рефлюкс, уретерогидронефроз, мегауретер, аномалии почек, гипоспадия, эписпадия, инфравезикальная обструкция, крипторхизм и др.).</li>
