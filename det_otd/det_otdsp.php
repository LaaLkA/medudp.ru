                <p>В отделении специализированной педиатрической помощи принимают специалисты: </p>
                <ul>
                    <li>Кабинет врача акушера-гинеколога</li>
                    <li>Кабинет врача аллерголога-иммунолога</li>
                    <li>Кабинет врача дерматовенеролога</li>
                    <li>Кабинет врача детского кардиолога</li>
                    <li>Кабинет врача оториноларинголога</li>
                    <li>Кабинет врача офтальмолога</li>
                    <li>Кабинет функциональных исследований</li>
                    <li>Кабинет травматолога-ортопеда</li>
                    <li>Хирургический кабинет</li>
                </ul>
                <p><b>Когда необходимо обратиться с ребенком к врачу оториноларингологу:</b></p>
                <ul>
                    <li>при появлении болей в ушах, горле, во время глотания, в области лба или околоносовых пазух
                        (отит, фарингит, ларингит, ангина, аденоидит, фронтит, гайморит и др.)
                    </li>
                    <li>при появлении отделяемого из уха, носа</li>
                    <li>при затрудненном носовом дыхании</li>
                    <li>при ухудшении дыхания ребенка во сне</li>
                    <li>при изменении голоса (осиплость или охриплость)</li>
                    <li>при головных болях, головокружениях, шуме в ушах</li>
                    <li>при отсутствии реакции на звуки, снижении слуха (тугоухость и др.)</li>
                    <li>при нарушении произношения звуков, задержке речевого развития</li>
                    <li>для исключения аномалий развития верхних дыхательных путей и органов слуха</li>
                </ul>
                <p><b>Какие клинико-лабораторные и инструментальные исследования может провести и назначить детский
                        оториноларинголог:</b></p>
                <ul>
                    <li>проведение оторинофарингоскопии (инструментальный осмотр)</li>
                    <li>проведение эндоскопического обследования</li>
                    <li>тимпанометрия (обследование среднего уха тимпанометром «Titan»)</li>
                    <li>отоакустическая эмиссия (ОАЭ), аудиологический скрининг (скрининговая система регистрации
                        вызванной отоакустической эмиссии для диагностики слуха новорожденных и детей 1-го года жизни
                        «OtoRead Interacoustics»)
                    </li>
                    <li>взятия мазка из зева на флору (с чувствительностью к антибиотикам и фагам)</li>
                    <li>назначение клинико-лабораторных исследований</li>
                    <li>назначение рентгенологического исследования придаточных пазух носа (ППН), МСКТ, МРТ по
                        показаниям
                    </li>
                </ul>

