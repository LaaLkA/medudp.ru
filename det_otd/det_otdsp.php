                <p>Методы лечения и профилактические мероприятия: оптическая коррекция, аппаратное лечение (вакуумные очки Сидоренко, цветоимпульсная терапия - визуальная цветостимуляция, магнитотерапия, тренировка аккомодации по методике Аветисова-Мац), массаж шейно-воротниковой зоны и спины, ЛФК, местное физиотерапевтическое лечение (УВЧ и др.), остеопатическое лечение, санаторно-курортное лечение.</p>
                <p>Также в кабинете врача офтальмолога активно работает Школа по профилактике и развитию близорукости у детей школьного возраста. Амбулаторно проводится курсовое консервативное лечение близорукости у детей такими методами как:</p>
