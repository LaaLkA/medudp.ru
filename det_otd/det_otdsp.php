                <p><b>В кабинете врача офтальмолога</b> оказывается лечебно-диагностическая и профилактическая медицинская помощь, диспансерное наблюдение детей всех возрастных групп с патологией рефракции (близорукостью, дальнозоркостью, астигматизмом), амблиопией, косоглазием, патологией сетчатки и оптических сред.</p>
                <p><b>Когда необходимо обратиться с ребенком к врачу офтальмологу:</b></p>
                <ul>
                    <li>при боли, покраснении, зуде, раздражении, светобоязни, слезотечении глаз и отделяемого из них</li>
                    <li>при снижении остроты зрения вблизи или вдаль</li>
                    <li>при головных болях, головокружениях, затуманивании зрения, появлении «мушек»</li>
                    <li>при длительных психоэмоциональных перенапряжениях, изменении в осанке, гиподинамии</li>
                    <li>при ограничении открывания глаз (птоз), непроизвольных движениях глазными яблоками, косоглазии</li>
