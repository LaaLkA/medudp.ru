                <p>Методы лечения и профилактические мероприятия: медикаментозное лечение по назначению врача, госпитализация в стационар, физиотерапевтическое лечение (КУФО, магнитолазерная терапия, УВЧ, ультразвуковая терапия, Биоптрон), массаж, ЛФК, санаторно-курортное лечение.</p>
                <p><b>Врач травматолог-ортопед</b> занимается диагностикой таких заболеваний, как врожденные и приобретенные патологии конечностей и позвоночника, нарушения походки, боль в суставах различного происхождения, а также лечением травм, гигром, кист с применением мало инвазивных методик.</p>
                <p>В зависимости от заболевания ребенка детский ортопед выбирает наиболее эффективный метод лечения: массаж, упражнения на специальных тренажерах, физиотерапевтические процедуры, лечебная гимнастика, использование ортопедических ортезов. Детский ортопед также определяет необходимость хирургического вмешательства при ортопедических заболеваниях.</p>
                <p>Для своевременного выявления врожденных заболеваний детский ортопед проводит плановый осмотр ребенка в возрасте 1 месяц жизни, далее в 3, 6 и 9 месяцев по назначению врача (в этот период на организм малыша приходится особенно большая нагрузка). Осуществление плановых профилактических осмотров у детского врача-ортопеда помогает на раннем этапе обнаружить и вылечить заболевания быстрее и эффективнее, поскольку в первый год жизни опорно-двигательный аппарат ребенка наиболее пластичен и способен к восстановлению.</p>
                <p><b>Когда необходимо обратиться с ребенком к врачу травматологу-ортопеду:</b></p>
                <ul>
                    <li>при появлении болей, отека, хруста в области суставов, позвоночнике, нарушении биомеханики походки и др. (для исключения острой травмы костно-мышечной системы, синовиита, гемартроза, артрита, хронических нарушений костно-мышечной системы и др.)</li>
                    <li>при острых травмах, порезах, укусах животными (для обработки ран и вакцинопрофилактики против бешенства и столбняка)</li>
                    <li>для исключения аномалий развития  органов и систем, других генетически связанных заболеваний</li>
                </ul>
                <p><b>Какие клинико-лабораторные и инструментальные исследования может провести и назначить детский травматолог-ортопед:</b></p>
                <ul>
                    <li>взятия мазка на флору (с чувствительностью к антибиотикам) с открытой раны</li>
                    <li>назначение клинико-лабораторных исследований </li>
                    <li>назначение ультразвуковых и рентгенологических исследований, МСКТ, МРТ по показаниям</li>
                </ul>
                <p>Может потребоваться дополнительная консультация врачей специалистов: хирурга, уролога-андролога, гастроэнтеролога, дерматолога, невролога, офтальмолога, кардиолога, инфекциониста, стоматолога,  оториноларинголога и др.</p>
                <p>Методы лечения и профилактические мероприятия: медикаментозное лечение по назначению врача, наложение гипсовых и иных иммобилизационных повязок, ортезирование, применение ортопедических стелек, местное хирургическое лечение под местной анестезией, физиотерапевтическое лечение (КУФО, магнитолазерная терапия, УВЧ, ультразвуковая терапия, Биоптрон), массаж, ЛФК, остеопатическое лечение, санаторно-курортное лечение.</p>

                <p>Врач аллерголог-иммунолог наблюдает детей с респираторными и кожными проявлениями аллергии. Для диагностики аллергических заболеваний проводятся скаррификационные тесты с полным набором отечественных и импортных аллергенов (бытовые, эпидермальные, пыльцевые, пищевые). В период обострения основного заболевания проводится лабораторная диагностика определения специфических иммуноглобулинов класса Е (импортные тест- системы). Для выявления бронхообструктивного синдрома проводится спирометрия, пробы с бронхолитиками. По показаниям используется аллергоспецифическая иммунотерапия (АСИТ), как импортными «Фосталь», так и отечественными антигенами. Врач кабинета имеет большой опыт в проведении сублингвальной АСИТ. Часто болеющим детям осуществляется коррекция иммунного статуса с предварительной лабораторной диагностикой и подбором необходимых иммунокорректирующих препаратов. Широко используются возможности кабинета ЛФК для обучения и проведения дыхательной гимнастики. Ежемесячно проводится обучение родителей и детей в «Астма-школе».</p>

                <p>Врач дерматовенеролог проводит диагностику и лечение острых и хронических заболеваний кожи у детей с рождения и до 18 лет, грибковых заболеваний, подбор индивидуальных средств по уходу и лечению за чувствительной детской кожей.</p>
                <p>В кабинете проводится активное обучение родителей и детей уходу за кожей, склонной к атопии и следующие методы исследований:</p>
                <ul>
                    <li>Дерматоскопия</li>
                    <li>Люминесцентная диагностика (осмотр кожных покровов под лампой Вуда)</li>
                    <li>Видеодерматоскопия диагностическая (картирование тела) </li>
                    <li>Видеодерматоскопия диагностическая образования кожи </li>
                    <li>Забор материала для лабораторной диагностики паразитарных и инфекционных заболеваний кожи и ногтевых пластинок</li>
                </ul>
                
                <p><b>Когда необходимо обратиться с ребенком к врачу  гинекологу:</b></p>
