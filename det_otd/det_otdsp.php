                    <li>осмотр и фотографирование глазного дна (сетчатки) с помощью немидриатической ретинальной фундус камеры Canon CR-2 AF  (без предварительного расширения зрачка)</li>
                    <li>взятия мазка с конъюнктивы глаза на флору (с чувствительностью к антибиотикам)</li>
                    <li>назначение ОКТ, МРТ головного мозга, УЗИ глазного яблока по показаниям</li>
