                    <h3><b>Рефлексотерапия</b></h3>

                    <p>Основой современной медицины является медикаментозная терапия с её огромным, всевозрастающим
                        количеством химических препаратов. В результате их одновременного назначения стали наблюдаться
                        неблагоприятные реакции, а в некоторых случаях стала развиваться «лекарственная болезнь».</p>

                    <p>В связи с этим возрос интерес к лечению традиционными способами. На этом фоне особо выделяется
                        восточная народная медицина. Она достаточно цельной дошла до наших дней, имеет свою систему
                        диагностики и лечения. Её традиционные способы воздействия на организм человека (иглоукалывание,
                        прижигание, массаж) официально признаны лечебным методом и наравне с современной медициной
                        применяются в комплексном лечении пациентов. Лечение способами акупунктуры по своему воздействию
                        на организм – это рефлекторный процесс, в основе которого лежит сложная нейрогуморальная
                        реакция, возникающая в ответ на дозированное раздражение определённых участков кожного покрова.
                        Этот процесс оказывает стимулирующее, регулирующее и нормализующее влияние на общую и системную
                        реактивность, повышает защитные силы организма, активизирует гипофизарно-надпочечниковую
                        систему, устраняет вегетативные, вегетативно-сосудистые нарушения.</p>

                    <p class="ul_title_rehabil">Рефлексотерапию широко применяют при следующих заболеваниях и
                        состояниях:</p>
                    <ul class="ul_rehabil">
                        <li>заболеваниях периферической нервной системы (радикулиты, невриты, парезы, параличи,
                            невралгии);
                        </li>
                        <li>неврозах (неврастения, истерия, логоневроз, ночной энурез);</li>
                        <li>функциональных гиперкинезах (тики, блефароспазм);</li>
                        <li>вегетативно-сосудистой дистонии;</li>
                        <li>начальных проявлениях атеросклероза, эндартериита, ревматического васкулита;</li>
                        <li>вегетативно-сосудистых пароксизмах периферического генеза (симпатоганглиониты, соляриты,
                            болезнь Рейно, мигрень, болезнь Меньера);
                        </li>
                        <li>эндокринных нарушениях (гипоталамический синдром, тиреотоксикоз, диабет, климакс);</li>
                        <li>вазомоторно-аллергических состояниях (крапивница, отёк Квинке, вазомоторный ринит);</li>
                        <li>бронхолёгочных заболеваниях (бронхиты, бронхиальная астма) ;</li>
                        <li>патологии ЖКТ (дискинезии, язвы, эрозии);</li>
                        <li>заболеваниях опорно-двигательного аппарата обменного, ревматического, травматического
                            происхождения;
                        </li>
                        <li>кожных болезнях (нейродермиты, экземы и др.);</li>
                        <li>заболеваниях ЛОР-органов (риниты, синуситы, отиты, тонзиллиты и др.).</li>
                    </ul>

                    <p class="ul_title_rehabil">Зафиксированны данные о положительном влиянии рефлексотерапии при ряде
                        других заболеваний:</p>
                    <ul class="ul_rehabil">
                        <li>Остаточные явления нарушения мозгового кровообращения.</li>
                        <li>Эпилепсия.</li>
                        <li>Рассеянный склероз.</li>
                        <li>Паркинсонизм.</li>
                        <li>Прогрессирующая мышечная дистрофия.</li>
                        <li>Полиомиелит.</li>
                        <li>Детский церебральный паралич.</li>
                    </ul>

                    <p class="ul_title_rehabil1"><i class="fa fa-exclamation-circle" aria-hidden="true"></i>
                        Противопоказания</p>
                    <ul class="ul_rehabil1">
                        <li>новообразования любого характера и любой локализации;</li>
                        <li>острые инфекции, лихорадка;</li>
                        <li>хронические инфекции (туберкулёз, бруцеллёз) в стадии обострения;</li>
                        <li>органические заболевания сердца, лёгких, печени, почек в стадии декомпенсации;</li>
                        <li>резкое истощение (кахексия);</li>
                        <li>грудной и старческий (более 75 лет) возраст;</li>
                        <li>психозы;</li>
                        <li>острые болевые синдромы невыясненного генеза;</li>
                        <li>беременность;</li>
                        <li>состояние после тяжёлой физической нагрузки, бега, горячей ванны.</li>
                    </ul>
                    <p>Следует учитывать, что у некоторых пациентов имеется индивидуальная непереносимость отдельных
                        видов рефлексотерапии. Отдельные пациенты резистентны к данным процедурам. Имеют значение масса
                        тела, склероз сосудов, биохимические показатели крови и т.д.</p>

                    <p class="ul_title_rehabil">Основные методы рефлексотерапии</p>

                    <p>В настоящее время применяют различные методы рефлексотерапии при воздействии на акупунктурные
                        точки. Эффективность методов лечения зависит от их соответствия состоянию больного и
                        патологическим нарушениям. Наиболее эффективно комплексное лечение с применением различных
                        методов рефлексотерапии, поэтому целесообразно использовать сочетание различных методов во время
                        одного сеанса.</p>

                    <p><b>Классическая корпоральная иглотерапия</b> – метод лечения заболеваний с помощью воздействия на
                        точки акупунктуры иглами, вводимыми в определенные зоны кожи. Существует несколько техник
                        введения игл, которые обеспечивают минимальные болевые ощущения. Исходя из диагностических
                        методов, пациенту выставляется восточный диагноз, которому соответствует определенный набор
                        точек воздействия, либо точки подбираются в соответствии с правилами восточной медицины.
                        Продолжительность сеанса от 15 до 60 минут. Курс - от 2 до 20 сеансов</p>

                    <p><b>Прогревание и прижигание</b> (терморефлексотерапия, цзю) - один из основных методов лечения
                        восточной традиционной медицины наряду с методом классической акупунктуры. Прогревание -
                        умеренное тепловое воздействие на акупунктурную точку; прижигание - сильное тепловое воздействие
                        на акупунктурную точку с развитием местного ожога I-III степени. Классическим методом
                        воздействия в восточной медицине служит применение полынных сигар или конусов. Полынь не горит,
                        а тлеет. Тлеющий конец полынной сигареты даёт инфракрасное излучение, оказывающее тепловое
                        воздействие на ткани.</p>

                    <p><b>Поверхностная иглорефлексотерапия</b> включает воздействие на акупунктурные точки,
                        биологически активные зоны валиком, многоигольчатым молоточком или пучком игл.</p>

                    <p><b>Точечный массаж</b>- вид рефлексотерапии, при котором целенаправленное терапевтическое
                        воздействие на органы и ткани организма осуществляют путём механического раздражения
                        (надавливание и другие методы массажа) определённых акупунктурных точек.</p>

                    <p><b>Микроиглорефлексотерапия</b> - одна из разновидностей иглоукалывания, предназначена для
                        длительного (пролонгированного) воздействия на акупунктурные точки. Известна в Китае как
                        интрадермальная иглотерапия "pi-nei-zhen" , "пи-нэй-чжень" ) .</p>

                    <p><b>Аппликация пластин</b> - один из методов пролонгированной рефлексотерапии. Чаще используют
                        пластины из красной меди, золота, серебра (проба 999), нержавеющей высокосортной стали и цинка.
                        Аппликация медных пластин оказывает наибольший терапевтический эффект при дефиците меди в
                        организме, а также при болевых синдромах благодаря её противоболевому действию. Серебро
                        используют для ускорения регенерации тканей (при замедленном заживлении ран, гастритах,
                        циститах, вагинитах и др.), при ожирении. Аппликации пластин из свинца нашли применение при
                        заболеваниях костной системы экссудативном диатезе, хронических дерматитах, кератозах кожи,
                        облысении, заболеваниях селезёнки. В некоторых случаях целесообразно использовать пластины из
                        разнородных металлов, в частности из меди и цинка. Наложение таких пластин генерирует
                        определённой величины электрический заряд (его величина при прочих равных условиях зависит от
                        размера пластин), что значительно усиливает лечебный эффект.</p>

                    <p><b>Краниальная рефлексотерапия</b> - воздействие с помощью акупунктурных игл, электрического тока
                        и других методов на особые зоны, расположенные на волосистой части головы.</p>

                    <p><b>Лазерорефлексотерапия</b>- воздействие на различные участки тела, сосуды непрерывным
                        монохроматическим поляризованным красным светом длиной волны 630-900 нм или полупроводниковым
                        лазером. Гелий-неоновые лазеры с малой длиной волны - 630 нм (6328 ангстрем). Проникают
                        неглубоко. Полупроводниковые лазеры с большей длиной волны - 800-1500 нм (8000- 15 000
                        ангстрем). Проникают наиболее глубоко.</p>
