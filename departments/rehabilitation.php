                    <h3><b>Массаж</b></h3>

                    <p>Массаж (от франц. massage, masser – растирать, от араб. mass – касаться,
                        щупать) – лечебный метод, заключающийся в дозированном механическом и
                        рефлекторном воздействии на органы и ткани организма, осуществляемом рукой массажиста или
                        специальным аппаратом.
                    </p>

                    <p>В зависимости от того с какой целью применяется массаж и как осуществляется воздействие на
                        поверхность тела (т.е. какими средствами), различают гигиенический, лечебный, спортивный,
                        сегментарно-рефлекторный, косметический, аппаратный массаж и самомассаж.
                        Массаж может быть локальным (частным) и общим.
                    </p>

                    <p><b>Гигиенический массаж</b> – активное средство профилактики заболеваний и ухода за телом,
                        сохранения нормального функционального состояния организма, укрепления здоровья. Он бывает общим
                        и локальным и применяется отдельно или в сочетании с утренней гигиенической гимнастикой, в сауне
                        или русской бане и т.п. Гигиенический массаж часто выполняется в виде самомассажа.</p>

                    <p><b>Лечебный массаж</b> – это эффективный метод лечения травм и заболеваний. В зависимости от
                        характера функциональных нарушений он применяется в самых разнообразных модификациях, имеет свою
                        методику, показания и противопоказания. Как и гигиенический массаж, он оказывает общее и
                        локальное воздействие. В лечебном массаже выделяют четыре основные приёма - поглаживание,
                        растирание, разминание, вибрацию, а также активно-пассивные движения.</p>

                    <p><b>Реабилитационный массаж</b> – наиболее эффективен при функциональном лечении и восстановлении
                        физической работоспособности спортсмена после длительных перерывов в тренировке, после
                        оперативных вмешательств (удаление менисков, операция на ахилловом сухожилии и др.). Этот массаж
                        проводится в сочетании с ЛФК, механотерапией и др.</p>

                    <p>При простудных заболеваниях (бронхиты, пневмонии и пр.) впервые 2 – 5 дней показан баночный
                        массаж, позднее - перкуссионный массаж в сочетании с ингаляциями (лекарственные вещества и
                        кислород), а на ночь - согревающий массаж. В основе действия банок лежит рефлекторный метод,
                        основанный на возникновении застойной гиперемии (покраснения), раздражении кожных рецепторов
                        создавшимся в банке вакуумом.</p>

                    <p><b>Перкуссионный массаж</b> показан при бронхитах и пневмониях для улучшения крово- и
                        лимфообращения, улучшения легочной вентиляции.</p>

                    <p><b>Спортивный массаж</b> применяется для повышения функционального состояния спортсмена,
                        улучшения спортивной формы, снятия утомления, повышения физической работоспособности и
                        профилактики травм и заболеваний опорно–двигательного аппарата. Спортивный массаж включает в
                        себя предстартовый (мобилизационный) и восстановительный массаж.
                    </p>

                    <p><b>Предстартовый (мобилизованный) массаж</b> проводится перед тренировкой (соревнованием). Задача
                        его – подготовка нервно – мышечного аппарата, сердечно–сосудистой и дыхательных систем,
                        ускорение процесса вырабатываемости, предупреждение травм опорно-двигательного аппарата.
                        Восстановительный массаж показан после больших физических и психических нагрузок. Его цель -
                        восстановление функционального состояния спортсмена, повышение физической работоспособности,
                        снятие общего утомления.</p>

                    <p><b>Сегментарно-рефлекторный массаж</b> – это один из методов воздействия на рефлекторные зоны
                        кожной поверхности тела. При воздействии специальными массажными приёмами на сегменты спинного
                        мозга возникают так называемые кожно-висцеральные (внутренние) рефлексы, вызывающие изменения
                        деятельности внутренних органов и кровообращения в них. Механизм действия
                        сегментарно-рефлекторного массажа заключается в раздражении кожных рецепторов. Ответная реакция
                        на него зависит от интенсивности, продолжительности, площади и места воздействия, а также от
                        функционального состояния организма.</p>

                    <p><b>Косметический массаж</b> (только локального характера) применяется при уходе за нормальной
                        кожей, для предупреждения ее преждевременного старения, при различных косметических недостатках.
                    </p>

                    <p><b>Аппаратный массаж</b> осуществляется с помощью различных по устройству и физиологическому
                        воздействию на ткани аппаратов (вибромассажные, вакуумные, гидромассажные и другие). Он может
                        быть общим и локальным.</p>

                    <p><b>Самомассаж</b> – одно из средств ухода за телом, при комплексном лечении некоторых травм и
                        заболеваний опорно–двигательного аппарата, а также используется перед стартом, после
                        соревнования (для снятия утомления), в саунах и т.п. Он может быть как общим, так и локальным.
