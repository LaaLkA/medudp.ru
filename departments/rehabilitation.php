                    <p class="ul_title_rehabil">Основными показаниями к ЭКГ являются:</p>
                    <ul class="ul_rehabil">
                        <li>Подозрение на заболевание сердца и высокий риск в отношении этих заболеваний.</li>
                        <li>Перенесенный острый инфаркт миокарда, т. к. является основным методом диагностики и динамики
                            заболевания.
                        </li>
                        <li>Заболевания внутренних органов, эндокринных желез, нервной системы, после перенесенной
                            ангины, и при подозрении на вовлечение сердца в патологический процесс.
                        </li>
                        <li>Перед оперативным вмешательством.</li>
                        <li>При ухудшении состояния больных с заболеваниями сердца, при появлении болей в сердце,
                            возникновении аритмии.
                        </li>
                        <li>При массовых профилактических обследованиях населения.</li>
                    </ul>
                    <p class="ul_title_rehabil">ЭКГ с фармакологическими и функциональными пробами:</p>
                    <ul class="ul_rehabil">
                        <li><b>Проба с обзиданом.</b><br>
                            Для проведения пробы с обзиданом сначала записывают исходную ЭКГ. Ее регистрируют в 12
                            общепринятых отведениях в горизонтальном положении больного. Затем пациенту дают 40-80 мг
                            обзидана и проводят повторное электрокардиографическое исследование через 30, 60, 90, и 120
                            мин. после приема препарата. Грудные электроды помещают в одни и те же точки при проведении
                            пробы и при контрольном исследовании. Проба показана при наличии на исходной ЭКГ изменений
                            со стороны зубца Т и сегмента ST.
                        </li>
                        <li><b>Проба с гипервентиляцией.</b><br>
                            Перед исследованием регистрируют исходную ЭКГ в 12 общепринятых отведениях в положении
                            больного лежа. Затем, не снимая с больного электроды, просят больного сделать 20-30
                            форсированных глубоких вдохов и выдохов с большой частотой без перерыва в течение 20-30 с и
                            сразу же после этого производят повторное электрокардиографическое исследование.
                        </li>
                        <li><b>Ортостатическая проба.</b><br>
                            Для дифференциального диагноза органических и функциональных заболеваний сердца сравнивают
                            ЭКГ, записанные в положении лежа и стоя. Для этого регистрируют контрольную ЭКГ в
                            горизонтальном положении (12 общепринятых отведений), затем больного просят встать и
                            записывают ЭКГ в вертикальном положении – сразу после вставания, а затем через 30 сек, 3 и 5
                            мин, а иногда и 10 минут неподвижного стояния. Ортостатическая проба позволяет оценить
                            функциональное состояние сердечно-сосудистой системы. Для этого сравнивают ЭКГ, записанные в
                            горизонтальном и вертикальном положении больного. Изменение положения тела приводит к
                            некоторому перемещению сердца в полости грудной клетки, перераспределению крови,
                            рефлекторной тахикардии и, иногда, к изменениям ЭКГ.
                        </li>
                        <li><b>Проба с задержкой дыхания.</b><br>
                            Назначают пробу с задержкой дыхания на вдохе и значительно реже – на выдохе. Пробу провожят
                            в положении больного лежа, просят его сделать глубокий вдох и задержать дыхание. Затем на
                            задержке дыхания проводят съемку электрокардиограммы. При обнаружении патологии на пленке,
                            во время съемки ЭКГ, передают электрокардиограмму врачу функциональной диагностики, и, по
                            его распоряжению, пациент направляется к терапевту или кардиологу. При обнаружении острой
                            коронарной недостаточности больной остается лежать на кушетке под наблюдением, вызывается
                            бригада скорой неотложной помощи и лечащий врач. Здесь же в кабинете ЭКГ больному
                            оказывается неотложная помощь с последующей госпитализацией при необходимости.
                        </li>
                    </ul>
