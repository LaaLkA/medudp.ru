                    <p class="ul_title_rehabil1">Электрокардиография (ЭКГ)</p>

                    <p>Электрокардиография - наиболее распространенный в клинической практике метод диагностики
                        заболеваний сердца. Этот метод информативен в выявлении гипертрофии различных отделов сердца,
                        ишемической болезни сердца, инфаркта миокарда, нарушении ритма и проводимости.</p>

