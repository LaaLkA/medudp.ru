                    <h3><b>Теплолечение</b></h3>

                    <p>Теплолечение (термотерапия) - применение с лечебными целями нагретых тел, называемых
                        теплоносителями. Нагревание ткани происходит при непосредственном контакте с теплоносителем.</p>

                    <p>Теплолечение является одним из самых старых и самых распро¬страненных методов физиотерапии. </p>

                    <p>Тепловые воздействия вызывают изменение обмена веществ. При повышении температуры ткани на 1°
                        интенсивность обменных процессов в ней возрастает приблизительно на 10%. Для достиже¬ния
                        лечебного действия достаточно повысить температуру ткани на 4-5°, но нагревание должно быть
                        достаточно длительным, а тепловой поток равномерным и стабильным. В связи с этим теплоноситель
                        должен обладать определенными физическими свойствами, благодаря которым обеспечивается такое
                        нагревание. </p>

                    <p>Напоминаем о следующих основных эффектах тепла: антиспастический, болеутоляющий,
                        интенсифицирующий крово- и лимфообращение и обмен веществ в тканях. С последним эффектом связано
                        рассасывающее и регенера¬торное действие тепла, в частности при воспалительных процессах. </p>

                    <p class="ul_title_rehabil">Основные показания к теплолечебным процедурам:</p>
                    <ul class="ul_rehabil">
                        <li>Воспалительные процессы (без нагноения) в стадии разре¬шения</li>
                        <li>Дегенеративно-дистрофические и воспалительные заболевания опорно-двигательного аппарата.
                        </li>
                        <li>Заболевания и травмы периферической нервной системы.</li>
                        <li>Последствия травм с целью болеутоления (ушибы, растяжения связок).</li>
                        <li>Гипермоторные дискинезии внутренних органов.</li>
                        <li>Контрактуры мышц спастического типа (перед лечебной гимнастикой).</li>
                    </ul>
                    <p class="ul_title_rehabil1"><i class="fa fa-exclamation-circle" aria-hidden="true"></i> Основные
                        противопоказания к теплолечению:</p>
                    <ul class="ul_rehabil1">
                        <li>Острые и подострые стадии воспалительного процесса, гнойное воспаление, обострение
                            хронических заболеваний.
                        </li>
                        <li>Нарушение термической чувствительности кожи.</li>
                    </ul>
                    <p><b>Озокерит</b> (ozo-пахну, heros-воск, греч.), "пахнущий воском", горный воск. Это продукт
                        нефтяного происхождения, состоит главным образом из смеси твердых высокоплавких углеводородов
                        парафинового ряда с примесью жидких и газообразных углеводородов того же ряда. Озокерит является
                        естественной горной породой. Различные его месторождения отличаются по химическому составу,
                        различны по цвету: светло-зеленый, желтый, бурый, черный. При продолжительном хранении на
                        воздухе озокерит темнеет.</p>

                    <p>Получают озокерит, выпаривая породу в котлах или экстрагируя его органическими соединениями.
                        Далее производят частичную отгонку минеральных масел, полностью освобождают его от воды и
                        механических примесей. После обработки озокерит по виду напо¬минает пчелиный воск.</p>

                    <p>Температура плавления озокерита от 52 до 68°. Теплоемкость его выше, а теплопроводность ниже, чем
                        у парафина. Теплоудерживающая способность значительно больше таковой парафина, конвекция тепла
                        практически отсутствует.</p>

                    <p>Лечебное действие озокерита связывают не только с его особенностями как теплоносителя, но и с
                        наличием в его составе биологии чески активных веществ, проникающих через неповрежденную кожу.
                        Они оказывают ацетилхолиноподобное действие, повышав тонус парасимпатического отдела
                        вегетативной нервной системы. Имеются сведения о более активной регенерации периферические
                        нервов при их заболеваниях и травмах при лечении озокеритом по сравнению с парафином.
                        Упоминается о наличии в озокерите фолликулиноподобных веществ, в связи с чем рекомендуется
                        применение его при лечении женского бесплодия, связанного с недоразвитием яичников. </p>

