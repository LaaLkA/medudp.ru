                    <h3><b>Водолечение</b></h3>

                    <p>Душ Шарко – представляет собой вариант массажа, при котором используется струя воды. Он успешно
                        применяется в лечебных и реабилитационных целях. Сейчас процедура душа Шарко больше интересует
                        тех,
                        кто желает сделать фигуру красивой и стройной.</p>

                    <p>Падающие на тело больного струи воды вызывают деформацию кожи, сопровождающуюся раздражением, в
                        результате чего активизируется ток крови и лимфы по сосудам, увеличивается количество
                        функционирующих капилляров, кровь лучше питает органы, снабжая их кислородом и необходимыми для
                        полноценной жизнедеятельности веществами. Укрепляются стенки сосудов, улучшается кровоток,
                        улучшается работа лимфатической системы, повышается тонус мышц, усиливаются обменные процессы в
                        организме, снимаются последствия стрессов и нервных нагрузок, организм очищается от шлаков,
                        снижается воздействие свободных радикалов, нормализуется жировой обмен. Одновременно нарастает
                        содержание в коже активных веществ, которые в зависимости от температуры воды вызывают различные
                        изменения тонуса артериол и лимфатических сосудов.</p>

                    <p class="ul_title_rehabil">Показания к применению душа Шарко:</p>
                    <ul class="ul_rehabil">
                        <li>остеохондроз, спондилез;</li>
                        <li>артроз, артрит;</li>
                        <li>подагра;</li>
                        <li>реабилитация после переломов, а также травм мышц и связок;</li>
                        <li>миозит;</li>
                        <li>нарушение обмена веществ;</li>
                        <li>депрессия, стрессы, хроническая усталость;</li>
                        <li>снижение иммунитета;</li>
                        <li>ожирение;</li>
                        <li>целлюлит;</li>
                        <li>повышенные спортивные нагрузки.</li>
                    </ul>
                    <p>В косметологии метод активно используют как эффективное средство для борьбы с целлюлитом – кожа
                        после курса лечения становится более гладкой, упругой и эластичной. Кроме того, женщины с
                        радостью обнаруживают минус в сантиметрах на проблемных участках тела.</p>

                    <p>Важно помнить, что сама по себе процедура душа Шарко не поможет забыть о целлюлите и лишних
                        жировых отложениях.</p>

                    <p>Это лишь часть комплексного воздействия, которое также включает коррекцию питания и регулярную
                        физическую активность. Зачастую душ Шарко назначается не один, а в комплексе с такими
                        процедурами, как массаж, иглорефлексотерапия, прессотерапия (лимфодренаж), лечебные ванны и
