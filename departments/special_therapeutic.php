                    <p>Программа лечения сахарного диабета включает в себя:</p>
                    <ul>
                        <li>Использование современной стратегии оптимизации терапии сахарного диабета, основанной на
                            инновационных препаратах стандартах и алгоритмах
                        </li>
                        <li>Контроль лечения сахарного диабета включает исследования гликемии натощак, постпрандиальной
                            гликемии, гликированного гемоглобина
                        </li>
                        <li>Профилактика микро и макрососудистых осложнений</li>
                        <li>Проведение индивидуально для каждого больного расчета диеты с учетом калоража и хлебных
                            единиц (для лиц, получающих инсулин)
                        </li>
                        <li>Обучение самоконтролю (работа с глюкометром) и ведения дневников самоконтроля, технике
                            введения препаратов инсулина шприц-ручками
                        </li>
                        <li>Обеспечение литературой по осложнениям сахарного диабета и первой помощи при гипогликемии
                        </li>
                    </ul>
                    <p>Лечение сахарного диабета и его осложнений проводится совместно с окулистами, неврологами,
                        нефрологом, хирургами с использованием возможностей дневного стационара.</p>

                    <p>Совместно с акушерами-гинекологами осуществляются мероприятия по профилактике йоддефицитных
                        состояний у беременных и кормящих женщин.</p>

                    <p>Восстановительное лечение пациентов осуществляется в реабилитационном центре Управления делами
