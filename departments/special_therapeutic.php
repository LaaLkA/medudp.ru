            <p>В отделении специализированной медицинской помощи, предназначеной для взрослого населения, работают
                специалисты: кардиолог, нефролог, аллерголог-иммунолог, ревматолог, пульмонолог, эндокринолог.</p>

            <p>Высокий профессионализм и внимательное отношение к пациентам — отличительные черты персонала
                отделения.</p>

            <p>Основным направлением практической деятельности отделения является оказание комплексной
                высокоспециализированной помощи пациентам с острой и хронической соматической патологией, проведение
                диспансерно-профилактических мероприятий и обучающих программ для пациентов, оказание
                высокотехнологичной медицинской помощи.</p>

            <p>Сотрудники отделения принимают участие в научно- практических конференциях по специальности, участвуют в
                европейских симпозиумах, регулярно проходят переподготовку, осваивают и внедряют новые методики лечения,
                активно сотрудничают со смежными отделениями.</p>

            <p>Достижения науки и наш опыт позволяют добиваться успешного решения нашей главной задачи — лечения
                пациентов. Всестороннее обследование даёт возможность выбрать эффективный метод лечения с последующим
                контролем проводимой терапии, получить рекомендации по здоровому образу жизни и профилактики
                заболеваний.</p>

            <p>Мы будем рады помочь Вам. Желаем здоровья и благополучия.</p>

