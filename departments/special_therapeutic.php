                    </ul>
                    <p>В кабинете осуществляется:</p>
                    <ul>
                        <li>Проведение и оценка функции внешнего дыхания</li>
                        <li>Проведение диагностических проб с подбором брохолитиков для выявления бронхообструктивного
                            синдрома, рестриктивных нарушений и оценки эффективности лечения ингаляционными препаратами.
                        </li>
                        <li>Пульсоксиметрия</li>
                        <li>Пикфлуометрия</li>
                        <li>Небулайзерная терапия бронхолитиками, ингаляционными глюкокортикостероидами, муколитиками
                        </li>
                        <li>Респираторный мониторинг у пациентов с подозрением на СОАС (синдром обструктивного апноэ
                            сна).
                        </li>
                    </ul>
                    <p>Проводится широкий спектр обследований:</p>
                    <ul>
                        <li>Компьютерная диагностика бронхолегочных заболеваний</li>
                        <li>Исследование мокроты: цитологическое, посев с определением флоры и чувствительности к
                            антибиотикам.
                        </li>
                        <li>Определение атипичных возбудителей заболеваний легких (специфические IgE к хламидии
                            пневмония, микоплазме)
                        </li>
                        <li>Широко используются возможности кабинетов ЛФК, ФТЛ для проведения бронхолегочной
                            реабилитации.
                        </li>
                    </ul>
                    <p>Проводится индивидуальная астма-школа, программы по лечению никотиновой зависимости.</p>

