                    <h3></h3>

                    <p>Наблюдаются пациенты с ревматическими заболеваниями:</p>
                    <ul>
                        <li>Ревматоидный артрит</li>
                        <li>Серонегативные артриты</li>
                        <li>Псориатический артрит</li>
                        <li>Реактивные артриты</li>
                        <li>Болезнь Бехтерева</li>
                        <li>СЗСТ-системная склеродермия</li>
                        <li>Системная красная волчанка</li>
                        <li>Системные васкулиты</li>
                        <li>Остеопороз</li>
                        <li>Дегенеративные заболевания опорно-двигательного аппарата - остеоартроз.</li>
                    </ul>
                    <p>В целях ранней диагностики ревматической патологии и своевременного назначения базисной
                        противовоспалительной терапии проводится ряд клинико-лабораторных и инструментальных методов
                        исследования согласно алгоритмам европейской и американской лиги ревматологов:</p>
                    <ul>
                        <li>Антитела к циклическому цитрулиновому пептиду для диагностики ревматоидного артрита</li>
                        <li>Антинуклиарные антитела, антитела к двухспиральной ДНК, антифосфолипидные антитела,
                            волчаночный антикоагулянт с целью диагностики системных заболеваний соединительной ткани и
                            других системных заболеваний
                        </li>
                        <li>Антинетрофильные цитоплазматические антитела (АНЦА) для диагностики системных воскулитов
                        </li>
                        <li>HLA B27 для дифферинциальной диагностики серонегативных спондилоартритов</li>
                        <li>Рентгенологическое исследование опорно-двигательного аппарата, компьютерная томография,
                            КТ-остеоденситометрия
                        </li>
                        <li>УЗ-исследование суставов и околосуставных тканей</li>
                    </ul>
                    <p>В комплексном лечении заболеваний опорно-двигательного аппарата проводится подбор
                        медленнодействующих базисных препаратов, противовоспалительных средств, лечебно-диагностическая
                        пункция суставов, внутрисуставные введения лечебных препаратов.</p>

                    <p>Пациенты с заболеваниями опорно-двигательного аппарата могут пройти восстановительное лечение с
                        использованием лечебной физкультуры, современных физиотерапевтических методик, позволяющих
                        достигнуть положительного клинического эффекта в более короткие сроки. Применяется электро,
                        свето, бальнеотерапия. водолечение, магнитотерапия, терапевтический лазер. При необходимости
                        пациенты направляются на лечение в реабилитационный центр, в ревматологическое отделение
                        Центральной клинической больницы Управления делами Президента Российской Федерации.</p>
