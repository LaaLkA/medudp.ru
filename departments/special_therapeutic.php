                    <h3></h3>

                    <p>Кардиологи отделения проводят наблюдение и лечение пациентов с заболеваниями сердечно-сосудистой
                        системы:</p>
                    <ul>
                        <li>Ишемическая болезнь сердца</li>
                        <li>Артериальная гипертензия</li>
                        <li>Нарушения ритма и проводимости сердца</li>
                        <li>Врожденные и приобретенные пороки сердца</li>
                        <li>Ведение пациентов после имплантации кардиостимулятора, кардиовертера--дефибриллятора,
                            операций по поводу заболеваний сердца и сосудов (стентирование, аорто-коронарное
                            шунтирование, протезирование клапанов сердца и других)
                        </li>
                        <li>Дислипидемия</li>
                    </ul>
                    <p>Проводится амбулаторный этап реабилитации после инфаркта миокарда, АКШ (аорто-коронарное
                        шунтирование), а также обследование на наличие факторов риска развития сердечно-сосудистых
                        заболеваний, осложнений атеросклероза и артериальной гипертонии.</p>

                    <p>Для диагностики сердечно-сосудистых заболеваний используется широкий спектр инструментальных и
                        лабораторных исследований:</p>
                    <ul>
                        <li>электрокардиография</li>
                        <li>эхокардиография</li>
                        <li>тесты с физической нагрузкой ( в т.ч. медикаментозные)</li>
                        <li>суточное мониторирование ЭКГ</li>
                        <li>суточное мониторирование артериального давления</li>
                        <li>дуплексное исследование брахиоцефальных артерий, артерий нижних конечностей</li>
                        <li>липидный спектр крови, гемостаз, элетролиты, показатели свертываемости крови и т.д.</li>
                        <li>исследование содержания внутрикоронарного кальция.</li>
                    </ul>
                    <p>Кардиологический кабинет активно сотрудничает с отделениями кардиохирургического профиля
                        стационаров Управления делами Президента Российской Федерации и другими профильными
                        кардиохирургическими стационарами.</p>
