            <p>Стоматологическое отделение оснащено стоматологическими установками фирмы Simens (Германия), имеет зуботехническую лабораторию. В отделении работают квалифицированные детские врачи стоматологи-терапевты, хирург, ортодонт и зубной техник.</p>
            <p>Большое внимание уделяется профилактике стоматологических заболеваний. В отделении работает школа обучения гигиене полости рта с обязательным контролем гигиенических навыков. Обучение проводится с каждым пациентом на специальных моделях с использованием дополнительных пособий, выдаются памятки по обучению гигиене полости рта, профилактике кариеса, зубочелюстных аномалий и т.д. Проводится профессиональная гигиена полости рта с помощью различных щеток, паст, а также ручного инструмента, путем использования скейлера, аппарата Air-Flou. Применяются индивидуальные программы профилактики (рекомендации по режиму питания, подбор зубных щеток и паст в зависимости от проблем пациента и назначение Са-содержащих препаратов).</p>
            <p>Осуществляется покрытие зубов Ft-содержащими лаками и герметизация фиссур светоотверждаемыми материалами.</p>
            <p>Диагностика стоматологических заболеваний проводится современным методом оценки состояния твердых тканей зуба (аппарат Diagnodent).</p>
            <p>Детские врачи стоматологи-терапевты проводят лечение слизистых оболочек полости рта (в т.ч. различных стоматитов), кариеса и его осложненных форм (пульпиты, периодонтиты), косметическую реставрацию зубов у подростков, изготовление виниров светотверждаемыми материалами.</p>
            <p>В кабинете хирургической стоматологиии проводится:</p>
            <ul>
                <li>первичная хирургическая подготовка пациентов к ортодонтическому лечению</li>
                <li>пластика уздечек верхней и нижней губы, языка</li>
                <li>углубление сводов преддверия полости рта</li>
                <li>удаление сверхкомплектных и ретинированных зубов, в т.ч. зубов мудрости</li>
                <li>обнажение коронок ретинированных зубов для фиксации ортодонтических кнопок</li>
                <li>установка микроимплантов</li>
                <li>удаление молочных и постоянных зубов различной степени сложности.</li>
                <li>лечение острых воспалительных процессов в челюстно-лицевой области, в т.ч. лимфаденитов. сиаладенитов, обострения слюнокаменной болезни.</li>
                <li>удаление мелких новообразований и кист челюстно-лицевой области с последующим гистологическим исследованием</li>
            </ul>

