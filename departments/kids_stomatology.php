            <p>В ортодонтическом кабинете проводится лечение и профилактика зубо-челюстных аномалий у детей и подростков до 18 лет. Методы раннего ортодонтического лечения позволяют добиться формирования привильного прикуса. Вниманию наших маленьких пациентов мы представляем разноцветные пластинки, LM-активаторы, трейнеры, вестибулярные пластинки. Специальные пластинки Stoppi из мягкого силикона успешно заменяют малышу любимую пустышку или пальчик во рту. Профилактические пластинки Хинца помогают детям от 3 до 5 лет избавиться от вредных привычек и ротового дыхания. Для детей с различными парафункциями разработаны индивидуальные программы по миогимнастике с демонстрацией видеороликов.</p>
            <p>Начиная с 7-8 лет широко используется применение съемных пластиночных аппаратов различных конструкций, в том числе и функциональных.</p>
