            <p>В терапевтическом отделении работают врачи высшей и первой квалификационной категории, кандидаты медицинских наук. </p>
            <p>Врачи - терапевты осуществляют первичный прием больных и назначает соответствующее обследование и лечение. При сложной ситуации или необходимости в специфическом обследовании или лечении пациент может быть направлен к врачам узких специальностей (терапевтического или хирургического профилей). При выявлении острой патологии врач-терапевт может госпитализировать пациента в стационар. </p>
            <p>Используя многолетний опыт работы, современные методы диагностики и лечения, основанные на порядках, стандартах и клинических рекомендаций, врачи-терапевты проводят комплекс лечебно-оздоровительных мероприятий по поводу острых и хронических заболеваний, раннюю диагностику заболеваний сердечно - сосудистой системы, органов дыхания, мочевыделительной системы, органов кроветворения, патологии желудочно-кишечного тракта, онкологических заболеваний, а также рекомендуют профилактическую методы для предотвращения определенных заболеваний.</p>

            <p>В целях обеспечения доступности медицинской помощи и соблюдения сроков её ожидания в поликлинике организована работа дежурных врачей-терапевтов, которые осуществляют приём пациентов без предварительной записи, в порядке общей очереди.
            </p>
            <p>Более подробная информация о работе дежурных врачей-терапевтов предоставляется в медрегистратуре главного корпуса.</p>
