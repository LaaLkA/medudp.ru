            <h3>Как защитить себя от гриппа?</h3>
            <p><b>Ежегодная вакцинация - это наиболее эффективная мера профилактики  против  гриппа.</b> Она осуществляется  эффективными противогриппозными вакцинами, содержащими актуальные штаммы вирусов гриппа, рекомендованные Всемирной организацией здравоохранения на предстоящий эпидсезон. Вакцинация рекомендуется всем группам населения, но особенно показана контингенту из групп риска: детям, начиная с 6-ти месяцев; людям преклонного возраста, страдающим хроническими заболеваниями; медицинским работникам; учителям; студентам; работникам сферы обслуживания, транспорта. Вакцинация проводится не позднее, чем за 2 – 3 недели до начала эпидемического подъема заболеваемости.</p>
            <p><b>К мерам неспецифической профилактики гриппа относятся следующие:</b></p>
            <ul>
                <li>Сократите время пребывания в местах массовых скоплений людей и общественном транспорте.</li>
                <li>Избегайте тесных контактов с людьми, которые имеют признаки заболевания: чихают, кашляют.</li>
                <li>Тщательно мойте руки с мылом, промывайте полость носа.</li>
                <li>Регулярно проветривайте помещение, в котором находитесь, и делайте влажную уборку.</li>
                <li>Пользуйтесь маской в местах скопления людей.</li>
                <li>Употребляйте в пищу продукты, содержащие витамин С (клюква, брусника, лимон и др.), а также блюда с добавлением чеснока, лука.</li>
                <li>В целях повышения устойчивости организма к респираторным вирусам, в том числе к вирусам гриппа, используйте (по рекомендации врача) различные препараты и средства, повышающие иммунитет.</li>
                <li>В случае появления заболевших гриппом в семье или рабочем коллективе рекомендуется начать прием противовирусных препаратов с профилактической целью (с учетом противопоказаний и согласно инструкции по применению препарата).</li>
                <li>Ведите здоровый образ жизни (полноценный сон, сбалансированное питание, физическая активность).</li>
            </ul>

