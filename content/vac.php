            <h3>Что такое грипп и какова его опасность?</h3>
            <p><b>Грипп - это инфекционное заболевание.</b> Возбудителем его является вирус, который от инфицированных
                людей попадает в носоглотку окружающих. Грипп может перерасти в пневмонию, что опасно для людей,
                страдающих заболеванием сердца или легких. Грипп, как правило, вызывает высокую температуру и судороги у
                детей. Список возможных осложнений гриппа обширный: пневмония, ринит, синусит, бронхит, отит, миокардит
                и перикардит, миозит, поражения почек, оболочек головного и спинного мозга, сосудов. Также при гриппе
                обостряются имеющиеся хронические заболевания. Возбудители гриппа, вирусы типов А и В, отличаются
                агрессивностью и исключительно высокой скоростью размножения. За считанные часы после заражения вирус
                гриппа приводит к глубоким поражениям слизистой оболочки дыхательных путей, открывая возможности для
                проникновения в нее бактерий. Это объясняет большее число бактериальных осложнений, возникающих при
                гриппе. Также важной особенностью вирусов гриппа является их способность видоизменяться практически
                ежегодно появляются все новые варианты вирусов.</p>
            <p>Симптомы гриппа: жар, температура 37,5 – 39°С, головная боль, боль в мышцах, суставах, озноб, усталость,
                кашель, насморк или заложенный нос, боль (першение) в горле.</p>
            <h3>Что делать при заболевании гриппом?</h3>
            <p>Следует остаться дома, чтобы не только не заразить окружающих, но и вовремя заняться лечением, для чего
                необходимо немедленно обратиться к врачу. <span class="text-danger"><b>Самолечение при гриппе недопустимо.</b></span>
                Именно врач должен поставить диагноз и назначить необходимое лечение, соответствующее Вашему состоянию и
                возрасту. Необходимо строго выполнять все рекомендации лечащего врача: своевременно принимать лекарства
                и соблюдать постельный режим во время болезни, так как при заболевании увеличивается нагрузка на
                сердечно-сосудистую, иммунную и другие системы организма. Рекомендуется обильное питье горячий чай,
                клюквенный или брусничный морс, щелочные минеральные воды.</p>
            <p>При температуре 38 – 39°С вызовите участкового врача на дом либо бригаду «скорой помощи».</p>

