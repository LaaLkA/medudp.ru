                    <h1 class="inspection__title">Предварительные и периодические медицинские осмотры (профосмотр)</h1>
                    <h3 class="inspection__subtitle">Общая информация</h3>
                    <p class="inspection__text">Работа по проведению периодических и предварительных осмотров и экспертизе профессиональной пригодности осуществляется в полном соответствии с требованиями <b>приказа Министерства Здравоохранения России №29н от 28.01.2021 г.</b></p>
                    <p class="inspection__text">В поликлинике имеются все необходимые врачи-специалисты и диагностическая база для организации предварительных и периодических осмотров всем категориям работающих. Все врачи-специалисты, участвующие в проведении предварительных и периодических осмотров, имеют тематическое усовершенствование по специальности «Профпатология».</p>
                    <p class="inspection__text">В поликлинике организовано проведение предварительных и периодических медицинских осмотров для <b>физических лиц</b>. Для прохождения медицинского осмотра Вам необходимо предъявить направление от работодателя и оплатить прием врача - профпатолога (1 400 рублей), который определит объем и ориентировочную стоимость предварительного (периодического) медицинского осмотра, необходимого для оценки состояния здоровья и профпригодности в предполагаемой сфере деятельности (должности) в соответствии с приказом Минздрава России от 28.01.2021 №29н</p>
