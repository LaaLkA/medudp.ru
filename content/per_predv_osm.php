                <div class="col-xs-8 inspection__item left-menu">
                    <p class="inspection__title">Цели медицинских осмотров</p>
                    <p class="inspection__text">Обязательные предварительные медицинские осмотры при поступлении на работу проводятся с целью определения соответствия состояния здоровья лица, поступающего на работу, поручаемой ему работе.</p>

                    <p class="inspection__title">Виды медосмотров</p>
                    <p class="inspection__subtitle">Предварительный осмотр</p>
                    <p class="inspection__text">Проводится при приеме на работу с целью оценки состояния здоровья человека и его профпригодности в предполагаемой сфере деятельности (должности). Позволяет выявить наличие медицинских противопоказаний к работе, тем самым снизить риски для работодателя и работника.</p>
                    <p class="inspection__subtitle">Периодический осмотр</p>
                    <p class="inspection__text">Проводится ежегодно или 1 раз в 2 года. Работники до 21 года проходят периодический осмотр ежегодно. Периодичность прохождения медосмотра регламентируется приложением 1 приказа №29н. Проводится в целях охраны здоровья населения. Позволяет наблюдать за здоровьем сотрудников в динамике, выявить на ранней стадии профзаболевания и вовремя начать лечение.</p>

                    <p class="inspection__title">Что включать в себя медосмотр?</p>
